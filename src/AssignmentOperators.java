public class
AssignmentOperators {
    public static void main(String[] args) {
        int a = 15;
        a+=7;  // a = a+7
        int b = 14;
        b-=7; //b = b-7
        int c = 13;
        c*=7; // c * 7
        float d = 12f;
        d%=7; // shows remainder
        d/=6;// d / 6
        System.out.println(d);
        System.out.println(c);
        System.out.println(b);

        System.out.println(a);
    }
}
